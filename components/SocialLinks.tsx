type Props = {
  size?: 'sm' | 'md';
  className?: string;
  showLabels?: boolean;
};

export default function SocialLinks({ size = 'md', className, showLabels = false }: Props) {
  const iconSize = size === 'sm' ? 'h-4 w-4' : 'h-5 w-5';
  const btnSize = size === 'sm' ? 'h-9 w-9' : 'h-10 w-10';

  return (
    <div className={cn('flex flex-wrap items-center gap-2', className)}>
      {(Object.keys(icons) as SocialKey[]).map((key) => {
        const url = site.social[key]?.trim();
        const { label, path } = icons[key];
        const enabled = Boolean(url);

        const iconEl = (
          <svg className={iconSize} viewBox="0 0 24 24" aria-hidden>
            {path}
          </svg>
        );

        if (enabled) {
          return (
            <a
              key={key}
              href={url}
              target="_blank"
              rel="noopener noreferrer"
              className={cn(
                btnSize,
                'inline-flex items-center justify-center rounded-lg border border-line bg-surface-card text-zinc-400 transition hover:border-brand/40 hover:bg-brand-soft hover:text-white'
              )}
              aria-label={label}
              title={label}
            >
              {iconEl}
            </a>
          );
        }

        return (
          <span
            key={key}
            className={cn(
              btnSize,
              'inline-flex cursor-not-allowed items-center justify-center rounded-lg border border-line/60 bg-surface/50 text-zinc-600 opacity-50'
            )}
            title={`${label} — link coming soon`}
            aria-label={`${label} coming soon`}
          >
            {iconEl}
          </span>
        );
      })}
      {showLabels && (
        <p className="mt-2 w-full text-xs text-zinc-600">
          Add URLs in <code className="text-zinc-500">data/siteContent.ts → site.social</code>.
        </p>
      )}
    </div>
  );
}
